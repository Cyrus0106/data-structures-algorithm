my_dict = {'name': 'Emily', 'age': 20, 'gender': 'female'}
my_dict['age'] = 21
print(my_dict)

# time complexity of updating a dictionary is O(1)
# space complexity of updating a dictionary is O(1)

my_dict['address'] = '123 Main St'
print(my_dict)

# time complexity of adding a key-value pair to a dictionary is O(1)
# space complexity of adding a key-value pair to a dictionary is O(1)